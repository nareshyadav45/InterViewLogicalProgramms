package java8Durga;

public class StudentConRef {
	private String name;
	private int rollno;
	private int marks;
	private int age;
	public StudentConRef(String name, int rollno, int marks, int age) {
		
		this.name = name;
		this.rollno = rollno;
		this.marks = marks;
		this.age = age;
	}
	@Override
	public String toString() {
		return "StudentConRef [name=" + name + ", rollno=" + rollno + ", marks=" + marks + ", age=" + age + "]";
	}
	
	
		
		
		
		
		
		
	}


